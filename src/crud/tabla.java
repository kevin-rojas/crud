package crud;

/**
 *
 * @author kevin
 */
public class tabla {
    private int id;
    private String nombre;
    private String apellido;
    private int edad;

    public tabla(int id, String nombre, String apellido, int edad) {
        this.id = id;
        this.nombre = nombre;
        this.apellido = apellido;
        this.edad = edad;
    }

    public int getId() {
        return id;
    }

    public String getNombre() {
        return nombre;
    }

    public String getApellido() {
        return apellido;
    }

    public int getEdad() {
        return edad;
    }
    
    
}
